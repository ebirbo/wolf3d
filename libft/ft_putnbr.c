#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int i;

	i = 1;
	if (n < 0)
	{
		n = n * (-1);
		ft_putchar ('-');
	}
	while ((n / i) >= 10)
		i = i * 10;
	while ((i > 0))
	{
		ft_putchar(((n / i) % 10) + 48);
		i = i / 10;
	}
}
